let garden: string = 'Tulipa'


switch (garden) {
    case 'Rosa':
        console.log('Esta é uma rosa')
        break
    case 'Violeta':
        console.log('Esta é uma Violeta')
        break
    case 'Tulipa':
        console.log('Esta é uma tulipa')
        break
    default:
        console.log('Não conheço essa flor')
}


let day: number = 5


switch (day) {
    case 0:
        console.log('Domingo')
        break
    case 1:
        console.log('Segunda')
        break
    case 2:
        console.log('Terça')
        break
    case 3:
        console.log('Quarta')
        break
    case 4:
        console.log('Quinta')
        break
    case 5:
        console.log('Sexta-Feira')
        break
    default:
        console.log('Sabado')
}